interface IGetCardsByStageParams {
  type: 'primary' | 'wood' | 'paper' | 'glass' | 'iron' | 'plastic';
  stage: 1 | 2;
}

export function getCardsByStage({
  type,
  stage,
}: IGetCardsByStageParams): Card[] {
  const length = stage === 1 ? 6 : 10;
  const invalidLength = stage === 1 ? 2 : 4;

  const shuffledCards: Card[] = shuffleArray(cards);

  const sameTypeCards = shuffledCards.filter(c =>
    c.type.some(ct => ct === type),
  );
  const differentTypeCards = shuffledCards.filter(c =>
    c.type.some(ct => ct !== type),
  );

  const finalCards = [
    ...sameTypeCards.slice(sameTypeCards.length - length),
    ...differentTypeCards.slice(differentTypeCards.length - invalidLength),
  ];

  const slicedCards: Card[] = shuffleArray([...finalCards, ...finalCards]);

  return slicedCards;
}

export default cards;
